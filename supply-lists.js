function buildSupplyHtml(items) {
    if (!items || items.length === 0) return "";
    var h = '<ul class="supply-modal-list">';
    for (var i = 0; i < items.length; i++) {
        h += "<li>" + items[i] + "</li>";
    }
    h += "</ul>";
    return h;
}

function openSupplyList(grade) {
    var data = SUPPLY_DATA[grade];
    if (!data) { alert("Grade not found: " + grade); return; }

    // Remove existing overlay if any
    var existing = document.getElementById("supplyOverlay");
    if (existing) existing.parentNode.removeChild(existing);

    var body = "";
    body += '<div class="supply-print-header">';
    body += "<h2>St. Ann Catholic School</h2>";
    body += "<p>" + data.title + " Supply List &mdash; 2025-2026 School Year</p>";
    body += "</div>";
    body += buildSupplyHtml(data.items);

    for (var n = 0; n < data.notes.length; n++) {
        body += '<div class="supply-modal-note">';
        body += "<strong>" + data.notes[n].heading + "</strong>";
        body += buildSupplyHtml(data.notes[n].items);
        body += "</div>";
    }

    body += '<div class="supply-modal-footer-note">';
    body += "<p><em>Many items may need to be replaced throughout the year.</em></p>";
    body += "<p>Questions? Contact the school office at (910) 483-3216 or schooladmin@mysacs.org</p>";
    body += "</div>";

    var sub = data.subtitle ? " &bull; " + data.subtitle : "";

    var overlay = document.createElement("div");
    overlay.className = "supply-modal-overlay";
    overlay.id = "supplyOverlay";
    overlay.innerHTML = '<div class="supply-modal">' +
        '<div class="supply-modal-header">' +
            "<div>" +
                '<div class="supply-modal-school">St. Ann Catholic School</div>' +
                "<h2>" + data.title + " Supply List</h2>" +
                '<p class="supply-modal-subtitle">2025-2026 School Year' + sub + "</p>" +
            "</div>" +
            '<button class="supply-modal-close" id="supplyCloseBtn">&times;</button>' +
        "</div>" +
        '<div class="supply-modal-body" id="supplyPrintArea">' + body + "</div>" +
        '<div class="supply-modal-actions">' +
            '<button class="btn-primary" id="supplyPrintBtn"><i class="fas fa-print"></i> Print This List</button>' +
            '<a href="https://www.saintanncs.com/s/School-Supply-List-2025-2026-xjdc.pdf" target="_blank" class="btn-secondary" style="text-decoration:none;"><i class="fas fa-download"></i> Download Full PDF</a>' +
        "</div>" +
    "</div>";

    document.body.appendChild(overlay);
    document.body.style.overflow = "hidden";

    document.getElementById("supplyCloseBtn").addEventListener("click", closeSupplyList);
    document.getElementById("supplyPrintBtn").addEventListener("click", printSupplyList);
    overlay.addEventListener("click", function(e) {
        if (e.target === overlay) closeSupplyList();
    });
}

function closeSupplyList() {
    var overlay = document.getElementById("supplyOverlay");
    if (overlay) {
        overlay.parentNode.removeChild(overlay);
        document.body.style.overflow = "";
    }
}

function printSupplyList() {
    var area = document.getElementById("supplyPrintArea");
    if (!area) return;
    var w = window.open("", "_blank");
    w.document.write("<!DOCTYPE html><html><head><title>Supply List</title>" +
        "<style>" +
        "body{font-family:Georgia,serif;max-width:700px;margin:2rem auto;padding:0 1.5rem;color:#2c2420}" +
        ".supply-print-header{display:block;text-align:center;border-bottom:2px solid #1a2744;padding-bottom:1rem;margin-bottom:1.5rem}" +
        ".supply-print-header h2{font-size:1.4rem;color:#1a2744;margin:0 0 .25rem}" +
        ".supply-print-header p{font-size:1rem;color:#7b2d3a;margin:0}" +
        ".supply-modal-list{list-style:none;padding:0}" +
        ".supply-modal-list li{padding:.4rem 0;border-bottom:1px solid #e2ddd2;font-size:.95rem}" +
        ".supply-modal-list li:before{content:'\\2610  ';color:#7b2d3a;font-size:1.1rem}" +
        ".supply-modal-note{margin-top:1.25rem;padding:.75rem;background:#f5f2e8;border-left:3px solid #c9a84c}" +
        ".supply-modal-note strong{display:block;margin-bottom:.5rem;color:#1a2744}" +
        ".supply-modal-footer-note{margin-top:1.5rem;padding-top:1rem;border-top:1px solid #e2ddd2;font-size:.85rem;color:#5a524d}" +
        ".supply-modal-footer-note em{color:#7b2d3a}" +
        "</style></head><body>" + area.innerHTML + "</body></html>");
    w.document.close();
    w.focus();
    setTimeout(function() { w.print(); }, 300);
}

// Attach click events when DOM is ready
document.addEventListener("DOMContentLoaded", function() {
    var cards = document.querySelectorAll(".supply-card");
    for (var i = 0; i < cards.length; i++) {
        cards[i].addEventListener("click", function() {
            var grade = this.getAttribute("data-grade");
            openSupplyList(grade);
        });
    }

    document.addEventListener("keydown", function(e) {
        if (e.key === "Escape") closeSupplyList();
    });
});
